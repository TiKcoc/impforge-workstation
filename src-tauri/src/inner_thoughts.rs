/// Return current pending suggestions (max `max_suggestions` from config).
/// Only returns suggestions when the user is interruptible (not typing, idle enough).
#[tauri::command]
pub async fn thoughts_get_suggestions(
    engine: tauri::State<'_, InnerThoughtsEngine>,
) -> AppResult<Vec<Suggestion>> {
    let config = engine.config.read().clone();
    if !config.enabled {
        return Ok(Vec::new());
    }

    let user_state = engine.user_state.read().clone();
    if !user_state.interruptible || user_state.is_typing {
        return Ok(Vec::new());
    }

    let suggestions = engine.suggestions.read().clone();
    let max = config.max_suggestions as usize;
    Ok(suggestions.into_iter().take(max).collect())
}

/// User dismissed a suggestion — record negative feedback.
#[tauri::command]
pub async fn thoughts_dismiss(
    engine: tauri::State<'_, InnerThoughtsEngine>,
    suggestion_id: String,
) -> AppResult<()> {
    let thought_id = {
        let mut suggestions = engine.suggestions.write();
        let pos = suggestions.iter().position(|s| s.id == suggestion_id);
        if let Some(idx) = pos {
            let removed = suggestions.remove(idx);
            Some(removed.thought_id)
        } else {
            None
        }
    };

    // Mark the underlying thought as dismissed
    if let Some(tid) = thought_id {
        let mut thoughts = engine.thoughts.write();
        if let Some(thought) = thoughts.iter_mut().find(|t| t.id == tid) {
            thought.dismissed = true;
            // Persist updated state (fire-and-forget)
            let _ = persist_thought(thought);
        }
    }

    log::info!("Suggestion dismissed: {suggestion_id}");
    Ok(())
}

/// User accepted a suggestion — record positive feedback.
#[tauri::command]
pub async fn thoughts_accept(
    engine: tauri::State<'_, InnerThoughtsEngine>,
    suggestion_id: String,
) -> AppResult<()> {
    let thought_id = {
        let mut suggestions = engine.suggestions.write();
        let pos = suggestions.iter().position(|s| s.id == suggestion_id);
        if let Some(idx) = pos {
            let removed = suggestions.remove(idx);
            Some(removed.thought_id)
        } else {
            None
        }
    };

    // Mark the underlying thought as surfaced (accepted)
    if let Some(tid) = thought_id {
        let mut thoughts = engine.thoughts.write();
        if let Some(thought) = thoughts.iter_mut().find(|t| t.id == tid) {
            thought.surfaced = true;
            let _ = persist_thought(thought);
        }
    }

    log::info!("Suggestion accepted: {suggestion_id}");
    Ok(())
}

/// Generate thoughts based on current context.
/// Uses Ollama to analyze the context and produce scored thoughts.
#[tauri::command]
pub async fn thoughts_generate(
    engine: tauri::State<'_, InnerThoughtsEngine>,
    context: String,
) -> AppResult<Vec<Thought>> {
    let config = engine.config.read().clone();
    if !config.enabled {
        return Ok(Vec::new());
    }

    let user_state = engine.user_state.read().clone();
    let min_score = config.min_score_to_surface;
    let max_suggestions = config.max_suggestions as usize;

    let generated = generate_thoughts_internal(&context, &user_state, min_score).await?;

    let mut new_thoughts = Vec::new();
    let mut new_suggestions = Vec::new();
    let now = Utc::now().to_rfc3339();

    for (thought, title, action_label, action_route) in &generated {
        new_thoughts.push(thought.clone());

        // Create suggestion from thought
        let suggestion = Suggestion {
            id: Uuid::new_v4().to_string(),
            thought_id: thought.id.clone(),
            title: title.clone().unwrap_or_else(|| {
                thought.content.chars().take(50).collect::<String>()
            }),
            description: thought.content.clone(),
            action_label: action_label.clone(),
            action_route: action_route.clone(),
            priority: score_to_priority(thought.score),
            created_at: now.clone(),
        };
        new_suggestions.push(suggestion);

        // Persist each thought to disk
        let _ = persist_thought(thought);
    }

    // Add to engine state (capped at max_suggestions)
    {
        let mut thoughts = engine.thoughts.write();
        thoughts.extend(new_thoughts.clone());
        // Keep last 100 thoughts in memory
        if thoughts.len() > 100 {
            let drain_count = thoughts.len() - 100;
            thoughts.drain(..drain_count);
        }
    }
    {
        let mut suggestions = engine.suggestions.write();
        suggestions.extend(new_suggestions);
        // Cap at max_suggestions
        suggestions.sort_by(|a, b| b.priority.cmp(&a.priority));
        suggestions.truncate(max_suggestions);
    }

    Ok(new_thoughts)
}

