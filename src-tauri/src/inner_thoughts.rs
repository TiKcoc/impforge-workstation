/// Schema for the JSON array Ollama should return.
#[derive(Debug, Deserialize)]
struct OllamaThoughtItem {
    content: String,
    #[serde(default)]
    relevance: f32,
    #[serde(default)]
    urgency: f32,
    #[serde(default)]
    novelty: f32,
    #[serde(default)]
    confidence: f32,
    #[serde(default)]
    title: Option<String>,
    #[serde(default)]
    action_label: Option<String>,
    #[serde(default)]
    action_route: Option<String>,
}

/// Ollama /api/chat response (non-streaming).
#[derive(Debug, Deserialize)]
struct OllamaChatResponse {
    #[serde(default)]
    message: Option<OllamaChatMsg>,
}

#[derive(Debug, Deserialize)]
struct OllamaChatMsg {
    #[serde(default)]
    content: String,
}

/// Resolve the Ollama URL from environment or default.
fn resolve_ollama_url() -> String {
    std::env::var("OLLAMA_URL")
        .or_else(|_| std::env::var("OLLAMA_HOST"))
        .unwrap_or_else(|_| "http://localhost:11434".to_string())
        .trim_end_matches('/')
        .to_string()
}

/// Call Ollama chat endpoint (non-streaming) for thought generation.
async fn ollama_generate(
    model: &str,
    system_prompt: &str,
    user_prompt: &str,
) -> Result<String, ImpForgeError> {
    let url = resolve_ollama_url();
    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(60))
        .build()
        .map_err(|e| ImpForgeError::internal("HTTP_CLIENT_ERROR", e.to_string()))?;

    let body = serde_json::json!({
        "model": model,
        "messages": [
            { "role": "system", "content": system_prompt },
            { "role": "user", "content": user_prompt },
        ],
        "stream": false,
        "options": {
            "temperature": 0.7,
            "num_predict": 512,
        }
    });

    let resp = client
        .post(format!("{url}/api/chat"))
        .json(&body)
        .send()
        .await
        .map_err(|e| {
            if e.is_connect() {
                ImpForgeError::service(
                    "OLLAMA_UNREACHABLE",
                    "Cannot connect to Ollama for thought generation",
                )
                .with_suggestion("Start Ollama with: ollama serve")
            } else if e.is_timeout() {
                ImpForgeError::service("OLLAMA_TIMEOUT", "Ollama thought generation timed out")
            } else {
                ImpForgeError::service("OLLAMA_REQUEST_FAILED", e.to_string())
            }
        })?;

    if !resp.status().is_success() {
        let status = resp.status();
        let body_text = resp.text().await.unwrap_or_default();
        return Err(ImpForgeError::service(
            "OLLAMA_HTTP_ERROR",
            format!("Ollama returned HTTP {status}"),
        )
        .with_details(body_text));
    }

    let parsed: OllamaChatResponse = resp.json().await.map_err(|e| {
        ImpForgeError::service("OLLAMA_PARSE_ERROR", format!("Failed to parse Ollama response: {e}"))
    })?;

    Ok(parsed
        .message
        .map(|m| m.content)
        .unwrap_or_default())
}

/// Extract a self-reported confidence score from an Ollama response.
/// Asks the model to rate its own confidence 1–10 and normalizes to 0.0–1.0.
async fn ollama_confidence_check(
    model: &str,
    original_prompt: &str,
    response: &str,
) -> f32 {
    let system = "You are a confidence evaluator. Rate the quality and accuracy of the given response on a scale of 1-10. Respond with ONLY a single integer.";
    let prompt = format!(
        "Original question: {}\n\nResponse to evaluate: {}\n\nConfidence rating (1-10):",
        original_prompt, response
    );

    match ollama_generate(model, system, &prompt).await {
        Ok(rating_text) => {
            // Parse the first digit found
            rating_text
                .chars()
                .find(|c| c.is_ascii_digit())
                .and_then(|c| c.to_digit(10))
                .map(|d| (d as f32 / 10.0).clamp(0.1, 1.0))
                .unwrap_or(0.5)
        }
        Err(_) => 0.5, // Default confidence when check fails
    }
}

