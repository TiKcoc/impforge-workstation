/// What triggered the AI to form a thought.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "snake_case")]
pub enum TriggerType {
    /// User opened a file, switched module, or performed an action.
    UserActivity,
    /// New data available, service status changed.
    ContextChange,
    /// Scheduled check (e.g. "meeting in 15 min").
    TimeBasedReminder,
    /// AI noticed a pattern in user behavior.
    PatternDetected,
    /// Something went wrong in a background process.
    ErrorDetected,
    /// Team goal update or progress milestone.
    GoalProgress,
}

/// Priority level for surfacing suggestions.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq, PartialOrd, Ord)]
#[serde(rename_all = "snake_case")]
pub enum SuggestionPriority {
    Low,
    Medium,
    High,
    Critical,
}

/// An internal thought generated by the engine.
/// Thoughts are private — only surfaced as `Suggestion` when score is high enough.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Thought {
    pub id: String,
    pub content: String,
    pub trigger: TriggerType,
    /// How relevant is this to the user's current activity (0.0–1.0).
    pub relevance: f32,
    /// How time-sensitive is this (0.0–1.0).
    pub urgency: f32,
    /// How new or surprising is this information (0.0–1.0).
    pub novelty: f32,
    /// How confident is the AI in this thought (0.0–1.0).
    pub confidence: f32,
    /// Combined score: relevance×0.3 + urgency×0.3 + novelty×0.2 + confidence×0.2.
    pub score: f32,
    pub created_at: String,
    /// Whether this thought was surfaced to the user.
    pub surfaced: bool,
    /// Whether the user dismissed it (negative feedback).
    pub dismissed: bool,
}

/// A user-visible suggestion derived from a scored thought.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Suggestion {
    pub id: String,
    pub thought_id: String,
    pub title: String,
    pub description: String,
    /// CTA button text (e.g. "Open", "Apply", "Fix").
    pub action_label: Option<String>,
    /// Route to navigate to when accepted (e.g. "/chat", "/ide").
    pub action_route: Option<String>,
    pub priority: SuggestionPriority,
    pub created_at: String,
}

/// Tracks what the user is currently doing so the engine knows when to interrupt.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct UserState {
    pub current_module: String,
    pub is_typing: bool,
    pub last_action_at: String,
    pub idle_seconds: u64,
    /// True if idle > threshold seconds — safe to show a suggestion.
    pub interruptible: bool,
}

impl Default for UserState {
    fn default() -> Self {
        Self {
            current_module: "home".to_string(),
            is_typing: false,
            last_action_at: Utc::now().to_rfc3339(),
            idle_seconds: 0,
            interruptible: true,
        }
    }
}

/// Configuration for the thoughts engine.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ThoughtsConfig {
    pub enabled: bool,
    /// Minimum combined score to surface a thought as a suggestion (0.0–1.0).
    pub min_score_to_surface: f32,
    /// Maximum suggestions queued at once.
    pub max_suggestions: u32,
    /// How often the engine checks for new thoughts (seconds).
    pub check_interval_seconds: u32,
    /// User must be idle this long before showing a suggestion.
    pub idle_threshold_seconds: u32,
}

impl Default for ThoughtsConfig {
    fn default() -> Self {
        Self {
            enabled: true,
            min_score_to_surface: 0.6,
            max_suggestions: 3,
            check_interval_seconds: 30,
            idle_threshold_seconds: 5,
        }
    }
}

/// Result of a cascade inference call.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CascadeResult {
    pub response: String,
    pub model_used: String,
    pub confidence: f32,
    /// Whether the request was escalated from local to cloud.
    pub escalated: bool,
    pub latency_ms: u64,
}

/// Aggregate statistics for cascade inference.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CascadeStats {
    pub total_inferences: u64,
    pub local_inferences: u64,
    pub cloud_inferences: u64,
    pub avg_local_confidence: f32,
    pub avg_latency_ms: f64,
    /// Estimated cloud API cost saved by using local models first.
    pub estimated_cost_saved_usd: f64,
}

