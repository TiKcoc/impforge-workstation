        // Perfect scores
        let score = calculate_score(1.0, 1.0, 1.0, 1.0);
        assert!((score - 1.0).abs() < f32::EPSILON);

        // Zero scores
        let score = calculate_score(0.0, 0.0, 0.0, 0.0);
        assert!((score - 0.0).abs() < f32::EPSILON);

        // Weighted: relevance=1, rest=0 → 0.3
        let score = calculate_score(1.0, 0.0, 0.0, 0.0);
        assert!((score - 0.3).abs() < 0.001);

