/// Update user state from the frontend (module change, typing state).
/// Called by the frontend on navigation and typing events.
#[tauri::command]
pub async fn thoughts_update_user_state(
    engine: tauri::State<'_, InnerThoughtsEngine>,
    module: String,
    is_typing: bool,
) -> AppResult<()> {
    let config = engine.config.read().clone();
    let now = Utc::now();
    let mut state = engine.user_state.write();

    // Calculate idle time from last action
    let last_action = chrono::DateTime::parse_from_rfc3339(&state.last_action_at)
        .map(|dt| dt.with_timezone(&Utc))
        .unwrap_or(now);
    let idle = (now - last_action).num_seconds().max(0) as u64;

    if is_typing || state.current_module != module {
        // User just did something — reset idle
        state.last_action_at = now.to_rfc3339();
        state.idle_seconds = 0;
        state.interruptible = false;
    } else {
        state.idle_seconds = idle;
        state.interruptible = idle >= config.idle_threshold_seconds as u64;
    }

    state.current_module = module;
    state.is_typing = is_typing;

    Ok(())
}

