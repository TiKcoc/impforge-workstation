/// Resolve the thoughts storage directory, creating it if needed.
fn thoughts_dir() -> Result<PathBuf, ImpForgeError> {
    let home = dirs::home_dir().ok_or_else(|| {
        ImpForgeError::filesystem("HOME_DIR_NOT_FOUND", "Cannot determine home directory")
    })?;
    let dir = home.join(".impforge").join("thoughts");
    if !dir.exists() {
        std::fs::create_dir_all(&dir).map_err(|e| {
            ImpForgeError::filesystem("THOUGHTS_DIR_CREATE_FAILED", e.to_string())
        })?;
    }
    Ok(dir)
}

/// Config file path.
fn config_path() -> Result<PathBuf, ImpForgeError> {
    let home = dirs::home_dir().ok_or_else(|| {
        ImpForgeError::filesystem("HOME_DIR_NOT_FOUND", "Cannot determine home directory")
    })?;
    let dir = home.join(".impforge");
    if !dir.exists() {
        std::fs::create_dir_all(&dir).map_err(|e| {
            ImpForgeError::filesystem("CONFIG_DIR_CREATE_FAILED", e.to_string())
        })?;
    }
    Ok(dir.join("thoughts_config.json"))
}

fn load_config_from_disk() -> Result<ThoughtsConfig, ImpForgeError> {
    let path = config_path()?;
    if !path.exists() {
        return Ok(ThoughtsConfig::default());
    }
    let data = std::fs::read_to_string(&path)?;
    serde_json::from_str(&data).map_err(|e| {
        ImpForgeError::config("CONFIG_PARSE_FAILED", format!("Failed to parse thoughts config: {e}"))
    })
}

fn save_config_to_disk(config: &ThoughtsConfig) -> Result<(), ImpForgeError> {
    let path = config_path()?;
    let data = serde_json::to_string_pretty(config).map_err(|e| {
        ImpForgeError::internal("CONFIG_SERIALIZE_FAILED", e.to_string())
    })?;
    std::fs::write(&path, data)?;
    Ok(())
}

/// Persist a thought to disk as a JSON file.
fn persist_thought(thought: &Thought) -> Result<(), ImpForgeError> {
    let dir = thoughts_dir()?;
    let filename = format!("{}.json", thought.id);
    let data = serde_json::to_string_pretty(thought).map_err(|e| {
        ImpForgeError::internal("THOUGHT_SERIALIZE_FAILED", e.to_string())
    })?;
    std::fs::write(dir.join(filename), data)?;
    Ok(())
}

/// Load thought history from disk (most recent first).
fn load_thought_history(limit: u32) -> Result<Vec<Thought>, ImpForgeError> {
    let dir = thoughts_dir()?;
    let mut entries: Vec<(std::time::SystemTime, PathBuf)> = Vec::new();

    let read_dir = std::fs::read_dir(&dir).map_err(|e| {
        ImpForgeError::filesystem("THOUGHTS_DIR_READ_FAILED", e.to_string())
    })?;

    for entry_result in read_dir {
        let entry = match entry_result {
            Ok(e) => e,
            Err(_) => continue,
        };
        let path = entry.path();
        if path.extension().and_then(|e| e.to_str()) != Some("json") {
            continue;
        }
        let modified = entry
            .metadata()
            .ok()
            .and_then(|m| m.modified().ok())
            .unwrap_or(std::time::SystemTime::UNIX_EPOCH);
        entries.push((modified, path));
    }

    // Sort newest first
    entries.sort_by(|a, b| b.0.cmp(&a.0));
    entries.truncate(limit as usize);

    let mut thoughts = Vec::with_capacity(entries.len());
    for (_, path) in entries {
        let data = match std::fs::read_to_string(&path) {
            Ok(d) => d,
            Err(_) => continue,
        };
        match serde_json::from_str::<Thought>(&data) {
            Ok(t) => thoughts.push(t),
            Err(_) => continue,
        }
    }
    Ok(thoughts)
}

