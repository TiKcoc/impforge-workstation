/// Calculate a combined score from the 4 dimensions.
/// Weights: relevance 30%, urgency 30%, novelty 20%, confidence 20%.
fn calculate_score(relevance: f32, urgency: f32, novelty: f32, confidence: f32) -> f32 {
    let r = relevance.clamp(0.0, 1.0);
    let u = urgency.clamp(0.0, 1.0);
    let n = novelty.clamp(0.0, 1.0);
    let c = confidence.clamp(0.0, 1.0);
    r * 0.3 + u * 0.3 + n * 0.2 + c * 0.2
}

/// Map a combined score to a suggestion priority.
fn score_to_priority(score: f32) -> SuggestionPriority {
    if score >= 0.9 {
        SuggestionPriority::Critical
    } else if score >= 0.75 {
        SuggestionPriority::High
    } else if score >= 0.6 {
        SuggestionPriority::Medium
    } else {
        SuggestionPriority::Low
    }
}

