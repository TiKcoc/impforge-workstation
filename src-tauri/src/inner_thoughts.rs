//! Inner Thoughts Engine — Proactive AI Suggestions + Cascade Inference
//!
//! Based on arXiv:2501.00383 "Proactive Conversational Agents with Inner Thoughts"
//! (CHI 2025, 82% user preference over non-proactive agents).
//!
//! **5-stage pipeline:**
//! 1. Trigger  — user activity, context change, pattern, error, time-based
//! 2. Retrieval — pull relevant memories from ForgeMemory
//! 3. Thought Formation — generate internal hypotheses via Ollama
//! 4. Evaluation — score by relevance × urgency × novelty × confidence
//! 5. Participation — surface suggestion only if score ≥ threshold AND user idle
//!
//! **Cascade Inference:**
//! Try Ollama (local, free) first. If model self-reports confidence < 0.8,
//! escalate to OpenRouter free-tier cloud model. Saves cost, preserves privacy.
//!
//! All thoughts persisted to `~/.impforge/thoughts/` as JSON files.

