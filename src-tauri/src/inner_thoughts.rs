/// The in-memory engine that Tauri manages via `app.manage()`.
pub struct InnerThoughtsEngine {
    pub thoughts: RwLock<Vec<Thought>>,
    pub suggestions: RwLock<Vec<Suggestion>>,
    pub user_state: RwLock<UserState>,
    pub config: RwLock<ThoughtsConfig>,
    // Cascade stats (atomic for lock-free counting)
    total_inferences: AtomicU64,
    local_inferences: AtomicU64,
    cloud_inferences: AtomicU64,
    total_local_confidence: RwLock<f64>,
    total_latency_ms: RwLock<f64>,
}

impl InnerThoughtsEngine {
    pub fn new() -> Self {
        let config = load_config_from_disk().unwrap_or_default();
        Self {
            thoughts: RwLock::new(Vec::new()),
            suggestions: RwLock::new(Vec::new()),
            user_state: RwLock::new(UserState::default()),
            config: RwLock::new(config),
            total_inferences: AtomicU64::new(0),
            local_inferences: AtomicU64::new(0),
            cloud_inferences: AtomicU64::new(0),
            total_local_confidence: RwLock::new(0.0),
            total_latency_ms: RwLock::new(0.0),
        }
    }

    /// Record a completed inference for statistics.
    fn record_inference(&self, local: bool, confidence: f32, latency_ms: u64) {
        self.total_inferences.fetch_add(1, Ordering::Relaxed);
        if local {
            self.local_inferences.fetch_add(1, Ordering::Relaxed);
            *self.total_local_confidence.write() += confidence as f64;
        } else {
            self.cloud_inferences.fetch_add(1, Ordering::Relaxed);
        }
        *self.total_latency_ms.write() += latency_ms as f64;
    }

    fn get_stats(&self) -> CascadeStats {
        let total = self.total_inferences.load(Ordering::Relaxed);
        let local = self.local_inferences.load(Ordering::Relaxed);
        let cloud = self.cloud_inferences.load(Ordering::Relaxed);
        let total_conf = *self.total_local_confidence.read();
        let total_lat = *self.total_latency_ms.read();

        let avg_local_conf = if local > 0 {
            (total_conf / local as f64) as f32
        } else {
            0.0
        };
        let avg_lat = if total > 0 {
            total_lat / total as f64
        } else {
            0.0
        };
        // Rough estimate: each cloud call ≈ $0.001 (free-tier models are $0)
        // but we count the "would have cost" at typical GPT-4o-mini rate
        let saved = local as f64 * 0.0005;

        CascadeStats {
            total_inferences: total,
            local_inferences: local,
            cloud_inferences: cloud,
            avg_local_confidence: avg_local_conf,
            avg_latency_ms: avg_lat,
            estimated_cost_saved_usd: saved,
        }
    }
}

