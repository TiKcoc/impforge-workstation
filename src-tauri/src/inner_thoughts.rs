/// Cascade inference: try local model first, escalate to cloud if confidence < threshold.
///
/// 1. Send prompt to Ollama (local, free, private).
/// 2. Ask Ollama to self-rate confidence (1–10).
/// 3. If confidence < `max_confidence` (default 0.8) → retry via OpenRouter free-tier.
/// 4. Return the best response with metadata.
#[tauri::command]
pub async fn cascade_infer(
    engine: tauri::State<'_, InnerThoughtsEngine>,
    prompt: String,
    max_confidence: Option<f32>,
) -> AppResult<CascadeResult> {
    let threshold = max_confidence.unwrap_or(0.8).clamp(0.1, 1.0);
    let start = std::time::Instant::now();

    // Step 1: Try local Ollama
    let local_model = std::env::var("IMPFORGE_CASCADE_MODEL")
        .unwrap_or_else(|_| "dolphin3:8b".to_string());

    let local_result = ollama_generate(
        &local_model,
        "You are a helpful AI assistant. Provide accurate, detailed answers.",
        &prompt,
    )
    .await;

    match local_result {
        Ok(response) if !response.trim().is_empty() => {
            // Step 2: Confidence check
            let confidence = ollama_confidence_check(&local_model, &prompt, &response).await;
            let latency_ms = start.elapsed().as_millis() as u64;

            if confidence >= threshold {
                // Local model is confident enough — use it
                engine.record_inference(true, confidence, latency_ms);
                return Ok(CascadeResult {
                    response,
                    model_used: local_model,
                    confidence,
                    escalated: false,
                    latency_ms,
                });
            }

            log::info!(
                "Cascade: local confidence {confidence:.2} < threshold {threshold:.2}, escalating to cloud"
            );

            // Step 3: Escalate to OpenRouter free-tier
            match escalate_to_cloud(&prompt).await {
                Ok(cloud_response) => {
                    let latency_ms = start.elapsed().as_millis() as u64;
                    engine.record_inference(false, confidence, latency_ms);
                    Ok(CascadeResult {
                        response: cloud_response,
                        model_used: "openrouter/free-tier".to_string(),
                        confidence: 0.9, // Cloud models generally more capable
                        escalated: true,
                        latency_ms,
                    })
                }
                Err(_) => {
                    // Cloud failed too — return local result anyway
                    engine.record_inference(true, confidence, latency_ms);
                    Ok(CascadeResult {
                        response,
                        model_used: local_model,
                        confidence,
                        escalated: false,
                        latency_ms,
                    })
                }
            }
        }
        Ok(_) | Err(_) => {
            // Local model unavailable or empty response — try cloud directly
            match escalate_to_cloud(&prompt).await {
                Ok(cloud_response) => {
                    let latency_ms = start.elapsed().as_millis() as u64;
                    engine.record_inference(false, 0.9, latency_ms);
                    Ok(CascadeResult {
                        response: cloud_response,
                        model_used: "openrouter/free-tier".to_string(),
                        confidence: 0.9,
                        escalated: true,
                        latency_ms,
                    })
                }
                Err(e) => {
                    Err(ImpForgeError::service(
                        "CASCADE_ALL_FAILED",
                        "Both local and cloud inference failed",
                    )
                    .with_details(e.to_string())
                    .with_suggestion("Check that Ollama is running, or configure an OpenRouter API key in Settings."))
                }
            }
        }
    }
}

/// Escalate a prompt to OpenRouter free-tier model.
async fn escalate_to_cloud(prompt: &str) -> Result<String, ImpForgeError> {
    let api_key = std::env::var("OPENROUTER_API_KEY").map_err(|_| {
        ImpForgeError::config(
            "MISSING_OPENROUTER_KEY",
            "No OpenRouter API key configured for cascade escalation",
        )
        .with_suggestion("Add your OpenRouter API key in Settings > API Keys.")
    })?;

    if api_key.is_empty() {
        return Err(ImpForgeError::config(
            "EMPTY_OPENROUTER_KEY",
            "OpenRouter API key is empty",
        ));
    }

    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(60))
        .build()
        .map_err(|e| ImpForgeError::internal("HTTP_CLIENT_ERROR", e.to_string()))?;

    let body = serde_json::json!({
        "model": "meta-llama/llama-4-scout:free",
        "messages": [
            { "role": "system", "content": "You are a helpful AI assistant. Provide accurate, detailed answers." },
            { "role": "user", "content": prompt },
        ],
        "max_tokens": 1024,
    });

    let resp = client
        .post("https://openrouter.ai/api/v1/chat/completions")
        .header("Authorization", format!("Bearer {api_key}"))
        .header("HTTP-Referer", "https://impforge.dev")
        .header("X-Title", "ImpForge Cascade Inference")
        .json(&body)
        .send()
        .await
        .map_err(|e| {
            ImpForgeError::service("OPENROUTER_REQUEST_FAILED", format!("OpenRouter request failed: {e}"))
        })?;

    if !resp.status().is_success() {
        let status = resp.status();
        let body_text = resp.text().await.unwrap_or_default();
        return Err(ImpForgeError::service(
            "OPENROUTER_HTTP_ERROR",
            format!("OpenRouter returned HTTP {status}"),
        )
        .with_details(body_text));
    }

    let parsed: serde_json::Value = resp.json().await.map_err(|e| {
        ImpForgeError::service("OPENROUTER_PARSE_ERROR", format!("Failed to parse OpenRouter response: {e}"))
    })?;

    let content = parsed
        .pointer("/choices/0/message/content")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .to_string();

    if content.is_empty() {
        return Err(ImpForgeError::service(
            "OPENROUTER_EMPTY_RESPONSE",
            "OpenRouter returned an empty response",
        ));
    }

    Ok(content)
}

